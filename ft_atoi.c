int	ft_atoi(const char *nptr)
{
	int	sign;
	int	i;
	int	value;

	i = 0;
	sign = 1;
	value = 0;
	while ((nptr[i] >= '\t' && nptr[i] <= '\r') || nptr[i] == ' ')
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		value = (value * 10) + (nptr[i] - '0');
		i++;
	}
	return (value * sign);
}
