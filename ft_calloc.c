#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*ret;

	if (!nmemb || !size)
		return (malloc(1));
	if (size * nmemb / nmemb != size)
		return (NULL);
	ret = malloc(nmemb * size);
	if (!ret)
		return (NULL);
	ft_bzero(ret, nmemb * size);
	return ((void *)ret);
}
